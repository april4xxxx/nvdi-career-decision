import { readFile, writeFile, mkdir } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { pathToFileURL } from "node:url";
import { createHash } from "node:crypto";

const MODULE_FIELDS = [
  "sop_id", "version", "status", "title", "career_phase", "journey_stages",
  "primary_topic", "secondary_topics", "supported_intents", "content_priority",
  "retrieval_tags", "recommendation_priority", "applicable_when",
  "not_applicable_when", "estimated_minutes", "tasks"
];

const FORBIDDEN_KEYS = new Set([
  "source_ids", "evidence_level", "sources", "references", "source_notes",
  "author", "authors", "platform", "url", "urls", "citation", "citations"
]);

function assert(condition, message) {
  if (!condition) throw new Error(message);
}

function assertNoForbiddenFields(value, location = "library") {
  if (Array.isArray(value)) {
    value.forEach((item, index) => assertNoForbiddenFields(item, `${location}[${index}]`));
    return;
  }
  if (!value || typeof value !== "object") return;
  for (const [key, child] of Object.entries(value)) {
    assert(!FORBIDDEN_KEYS.has(key.toLowerCase()), `${location}: forbidden field ${key}`);
    assertNoForbiddenFields(child, `${location}.${key}`);
  }
}

export function buildRuntimePackage(library, taxonomy) {
  assert(library?.library_id === "workplace_newcomer_year_one", "unexpected library_id");
  assert(library?.schema_version === "sop.v2.2", "unsupported schema_version");
  assert(Array.isArray(library?.modules), "library.modules must be an array");
  assertNoForbiddenFields(library);

  const phaseStages = new Set(taxonomy?.journey_stages?.posthire || []);
  const topics = new Set((taxonomy?.topics || []).map((topic) => topic.id));
  const intents = new Set(taxonomy?.intents || []);
  const ids = new Set();

  for (const module of library.modules) {
    assert(!ids.has(module.sop_id), `duplicate sop_id ${module.sop_id}`);
    ids.add(module.sop_id);
    assert(MODULE_FIELDS.every((field) => Object.hasOwn(module, field)), `${module.sop_id}: missing frozen field`);
    assert(Object.keys(module).every((field) => MODULE_FIELDS.includes(field)), `${module.sop_id}: unknown field`);
    assert(module.career_phase === "posthire", `${module.sop_id}: wrong career_phase`);
    assert(module.journey_stages.every((stage) => phaseStages.has(stage)), `${module.sop_id}: invalid stage`);
    assert(topics.has(module.primary_topic), `${module.sop_id}: invalid primary_topic`);
    assert(module.secondary_topics.every((topic) => topics.has(topic)), `${module.sop_id}: invalid secondary_topic`);
    assert(module.supported_intents.every((intent) => intents.has(intent)), `${module.sop_id}: invalid intent`);
  }

  const modules = library.modules
    .filter((module) => module.status === "ACTIVE")
    .map((module) => Object.fromEntries(MODULE_FIELDS.map((field) => [field, module[field]])));

  if (modules.length) {
    assert(library.status === "ACTIVE", "library must be ACTIVE before publishing ACTIVE modules");
  }

  return {
    runtime_schema: "sop-runtime.v1",
    library_id: library.library_id,
    source_schema_version: library.schema_version,
    source_version: library.version,
    source_sha256: createHash("sha256").update(JSON.stringify(library)).digest("hex"),
    taxonomy_sha256: createHash("sha256").update(JSON.stringify(taxonomy)).digest("hex"),
    release_status: modules.length ? "ACTIVE" : "BLOCKED_NO_ACTIVE_MODULES",
    source_updated_at: library.updated_at,
    module_count: modules.length,
    selection_rules: {
      max_modules_per_retrieval: 3,
      requires_user_confirmation: true,
      allow_no_match: true
    },
    modules
  };
}

function parseArgs(argv) {
  const args = {};
  for (let index = 0; index < argv.length; index += 2) {
    const key = argv[index];
    const value = argv[index + 1];
    if (!key?.startsWith("--") || !value) throw new Error("usage: --library FILE --taxonomy FILE --output FILE");
    args[key.slice(2)] = value;
  }
  return args;
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  assert(args.library && args.taxonomy && args.output, "library, taxonomy and output are required");
  const [library, taxonomy] = await Promise.all([
    readFile(resolve(args.library), "utf8").then(JSON.parse),
    readFile(resolve(args.taxonomy), "utf8").then(JSON.parse)
  ]);
  const runtimePackage = buildRuntimePackage(library, taxonomy);
  const output = resolve(args.output);
  await mkdir(dirname(output), { recursive: true });
  await writeFile(output, `// Generated by scripts/build-sop-runtime.mjs. Do not edit by hand.\nexport const SOP_RUNTIME_PACKAGE = ${JSON.stringify(runtimePackage, null, 2)};\n`, "utf8");
  process.stdout.write(JSON.stringify({ output, release_status: runtimePackage.release_status, module_count: runtimePackage.module_count }) + "\n");
}

